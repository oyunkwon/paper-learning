# --- synthetic user turn 헬퍼 (learning 차용) -------------------------------


def tutor_kickoff_user(chapter_id: str, chapter_title: str) -> str:
    """챕터 첫 진입 시 주입하는 합성 user 턴 (learning과 동일)."""
    return (
        f"'{chapter_title}' 챕터({chapter_id})를 시작하자. "
        f"첫 개념부터 설명해줘."
    )


def tutor_pass_user(chapter_id: str, next_index: int | None) -> str:
    """학습자가 현재 개념을 건너뛰기로 했을 때 주입하는 합성 user 턴. 이 한
    개념에 한해 '맞출 때까지 안 넘어감' 규칙을 무시하고 진행하게 한다
    (learning과 동일)."""
    if next_index is None:
        return (
            "이 개념은 건너뛸게. 이 챕터의 개념을 모두 다뤘으니, 챕터를 마무리하고 "
            "<<MASTERED:CHAPTER_ID>>를 출력해줘. (지금은 '넘어가지 마라' 규칙을 "
            "적용하지 말고, 학습자가 직접 건너뛰기를 선택한 것이다.)"
        ).replace("CHAPTER_ID", chapter_id)
    return (
        f"이 개념은 건너뛸게. 다시 묻지 말고 바로 다음 개념(인덱스 {next_index})으로 "
        f"넘어가서 설명을 시작해줘. 첫 줄에 <<TEACHING:{chapter_id}:{next_index}>>를 "
        f"넣어라. (지금은 '넘어가지 마라' 규칙을 적용하지 말 것. 학습자가 직접 "
        f"건너뛰기를 선택한 것이다.)"
    )
